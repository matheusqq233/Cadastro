        private void abrirCadAluno(){
        
        if(!flagGuiCadAluno){
            GUICadAluno gcp = new GUICadAluno();
            jdpAreaTrabalho.add(gcp);
            gcp.setVisible(true);
            
            flagGuiCadAluno = true;
            gcp.addInternalFrameListener(this);
        }//fim do if
    }//fim do m√©todo abrirCadProduto 
    
